using System;
using System.Collections.Generic;
using System.IO;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace FreeCodeReorganizer.Core;

/// <summary>
/// Reads the <c>.editorconfig</c> <c>generated_code = true</c> flag for a file, using the same Roslyn
/// machinery the C# compiler and <c>dotnet format</c> use. A project can mark whole folders as generated
/// (e.g. FreeCRM's <c>[**/EFModels/**.cs] generated_code = true</c>); the reorganizer then leaves those
/// files completely alone — no member reorder and no house-style formatting — matching how the formatter
/// and analyzers already skip them. This keeps the "don't touch generated code" intent in ONE place
/// (the .editorconfig that ships with the repo) instead of a parallel exclusion list.
/// </summary>
public sealed class GeneratedCodeDetector
{
    private readonly AnalyzerConfigSet? _set;

    private GeneratedCodeDetector(AnalyzerConfigSet? set) => _set = set;

    /// <summary>Builds a detector for a directory tree (collects the applicable .editorconfig files once).</summary>
    public static GeneratedCodeDetector ForRoot(string rootDir)
    {
        var configs = new List<AnalyzerConfig>();
        var seen = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

        try
        {
            string root = Path.GetFullPath(rootDir);

            // Ancestors (and the root itself), stopping once a config declares `root = true`.
            CollectAncestors(root, configs, seen);

            // Any nested .editorconfig files inside the tree (subfolder overrides).
            foreach (string cfg in Directory.EnumerateFiles(root, ".editorconfig", SearchOption.AllDirectories))
            {
                AnalyzerConfig? parsed = TryParseConfig(cfg, seen, out _);
                if (parsed is not null)
                {
                    configs.Add(parsed);
                }
            }
        }
        catch
        {
            // Best effort — a missing/locked config just means "nothing is marked generated".
        }

        return new GeneratedCodeDetector(configs.Count > 0 ? AnalyzerConfigSet.Create(configs) : null);
    }

    /// <summary>Convenience: true if a single file is marked generated by its applicable .editorconfig.</summary>
    public static bool IsFileGenerated(string filePath)
    {
        try
        {
            string? dir = Path.GetDirectoryName(Path.GetFullPath(filePath));
            if (dir is null)
            {
                return false;
            }

            var configs = new List<AnalyzerConfig>();
            CollectAncestors(dir, configs, new HashSet<string>(StringComparer.OrdinalIgnoreCase));
            if (configs.Count == 0)
            {
                return false;
            }

            return new GeneratedCodeDetector(AnalyzerConfigSet.Create(configs)).IsGenerated(filePath);
        }
        catch
        {
            return false;
        }
    }

    public bool IsGenerated(string filePath)
    {
        if (_set is null)
        {
            return false;
        }

        try
        {
            AnalyzerConfigOptionsResult options = _set.GetOptionsForSourcePath(Path.GetFullPath(filePath));
            return options.AnalyzerOptions.TryGetValue("generated_code", out string? value)
                && string.Equals(value, "true", StringComparison.OrdinalIgnoreCase);
        }
        catch
        {
            return false;
        }
    }

    // Walk up from a directory collecting each .editorconfig, stopping after one that declares root = true.
    private static void CollectAncestors(string startDir, List<AnalyzerConfig> configs, HashSet<string> seen)
    {
        DirectoryInfo? dir = new DirectoryInfo(startDir);
        while (dir is not null)
        {
            string cfg = Path.Combine(dir.FullName, ".editorconfig");
            if (File.Exists(cfg))
            {
                AnalyzerConfig? parsed = TryParseConfig(cfg, seen, out bool isRoot);
                if (parsed is not null)
                {
                    configs.Add(parsed);
                    if (isRoot)
                    {
                        break;
                    }
                }
            }

            dir = dir.Parent;
        }
    }

    private static AnalyzerConfig? TryParseConfig(string configPath, HashSet<string> seen, out bool isRoot)
    {
        isRoot = false;
        string full = Path.GetFullPath(configPath);
        if (!seen.Add(full))
        {
            return null;
        }

        try
        {
            string text = File.ReadAllText(full);
            isRoot = IsRootConfig(text);
            return AnalyzerConfig.Parse(SourceText.From(text), full);
        }
        catch
        {
            return null;
        }
    }

    // True if the preamble (before the first [section]) contains `root = true`.
    private static bool IsRootConfig(string text)
    {
        foreach (string raw in text.Split('\n'))
        {
            string line = raw.Trim();
            if (line.Length == 0 || line[0] == '#' || line[0] == ';')
            {
                continue;
            }

            if (line[0] == '[')
            {
                break; // reached the first section — preamble is over
            }

            int eq = line.IndexOf('=');
            if (eq > 0
                && line.Substring(0, eq).Trim().Equals("root", StringComparison.OrdinalIgnoreCase)
                && line.Substring(eq + 1).Trim().Equals("true", StringComparison.OrdinalIgnoreCase))
            {
                return true;
            }
        }

        return false;
    }
}
