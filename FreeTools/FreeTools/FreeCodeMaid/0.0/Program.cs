using System.Text;
using FreeCodeMaid;
using FreeTools.Core;

var argsList = args.ToList();

if (CliArgs.HasFlag(argsList, "--help", "-h", "-?"))
{
    PrintHelp();
    return 0;
}

bool apply = CliArgs.HasFlag(argsList, "--apply", "--write", "--fix");
CliArgs.HasFlag(argsList, "--check", "--dry-run"); // accepted for clarity; check is the default
bool verbose = CliArgs.HasFlag(argsList, "--verbose", "-v");
string? configPath = CliArgs.GetOption(argsList, "--config=");
bool noFormat = CliArgs.HasFlag(argsList, "--no-format");
// --report=<path> both enables the report AND sets its path; bare --report enables it with a
// default path. Parse the "=path" form first (GetOption removes it), then the bare flag.
string? reportPath = CliArgs.GetOption(argsList, "--report=");
bool report = reportPath is not null || CliArgs.HasFlag(argsList, "--report");
string target = CliArgs.GetPositional(argsList, 0) ?? Directory.GetCurrentDirectory();

ConsoleOutput.PrintBanner("FreeCodeMaid", "1.0");

ReorderConfig config;
string configSource;
try
{
    (config, configSource) = ReorderConfig.Resolve(configPath, target);
}
catch (Exception ex)
{
    ConsoleOutput.WriteLine($"Failed to load config: {ex.Message}", isError: true);
    return 2;
}

if (noFormat)
{
    config.RunFormatterFirst = false;
}

ConsoleOutput.PrintConfig("Target", Path.GetFullPath(target));
ConsoleOutput.PrintConfig("Mode", apply ? "APPLY (writing changes)" : "CHECK (dry run)");
ConsoleOutput.PrintConfig("Config", configSource);
ConsoleOutput.PrintConfig("Cleanup", (apply && config.RunFormatterFirst) ? "dotnet format whitespace, then reorganize" : "reorganize only");
ConsoleOutput.PrintConfig("Sort", config.SortAlphabetically ? "alphabetical within kind groups" : "kind groups only");
ConsoleOutput.PrintConfig("Group by access", config.GroupByVisibility.ToString());
ConsoleOutput.PrintDivider();
Console.WriteLine();

var (root, items) = FileScanner.Scan(target, config);
if (items.Count == 0)
{
    ConsoleOutput.WriteLine("No .cs files found at target.", isError: true);
    return 1;
}

if (apply && config.RunFormatterFirst)
{
    Console.WriteLine("Step 1/2 — running 'dotnet format whitespace' (Roslyn cleanup)...");
    bool formatOk = RunDotnetFormat(root);
    Console.WriteLine(formatOk
        ? "  cleanup complete."
        : "  (dotnet format did not finish cleanly — continuing with FreeCodeMaid's own passes)");
    Console.WriteLine("Step 2/2 — reorganizing members + restoring \"){\"...");
    Console.WriteLine();
}

int changed = 0, unchanged = 0, skipped = 0, errors = 0, typesSkipped = 0, bracesTotal = 0;
var reportEntries = new List<(string RelativePath, ReorgResult Result)>();

foreach (var item in items)
{
    if (item.Action == FileAction.SkipByPurpose)
    {
        skipped++;
        if (verbose)
        {
            Console.WriteLine($"  skip (by-purpose/generated): {item.RelativePath}");
        }
        continue;
    }

    string text;
    try
    {
        text = File.ReadAllText(item.AbsolutePath);
    }
    catch (Exception ex)
    {
        errors++;
        ConsoleOutput.WriteLine($"  ERROR read {item.RelativePath}: {ex.Message}", isError: true);
        continue;
    }

    if (config.SkipAutoGenerated && LooksAutoGenerated(text))
    {
        skipped++;
        if (verbose)
        {
            Console.WriteLine($"  skip (auto-generated): {item.RelativePath}");
        }
        continue;
    }

    string eol = text.Contains("\r\n") ? "\r\n" : "\n";
    var result = Reorganizer.Run(text, config, eol);
    typesSkipped += result.TypesSkipped;

    if (result.Error is not null)
    {
        errors++;
        ConsoleOutput.WriteLine($"  ERROR {item.RelativePath}: {result.Error}", isError: true);
        continue;
    }

    if (!result.Changed)
    {
        unchanged++;
        if (verbose)
        {
            Console.WriteLine($"  unchanged: {item.RelativePath}");
        }
        continue;
    }

    changed++;
    bracesTotal += result.BracesCollapsed;
    if (report)
    {
        reportEntries.Add((item.RelativePath, result));
    }
    var summary = DescribeChange(result);
    if (apply)
    {
        try
        {
            WritePreservingBom(item.AbsolutePath, result.NewText!);
            Console.WriteLine($"  fixed: {item.RelativePath} ({summary})");
        }
        catch (Exception ex)
        {
            errors++;
            ConsoleOutput.WriteLine($"  ERROR write {item.RelativePath}: {ex.Message}", isError: true);
        }
    }
    else
    {
        Console.WriteLine($"  would fix: {item.RelativePath} ({summary})");
    }
}

Console.WriteLine();
ConsoleOutput.PrintDivider("SUMMARY");
Console.WriteLine($"  Files changed:       {changed}{(apply ? "" : " (dry run — use --apply to write)")}");
Console.WriteLine("  Braces collapsed:    " + bracesTotal + " (wrapped-parameter braces restored to \"){\")");
Console.WriteLine($"  Files unchanged:     {unchanged}");
Console.WriteLine($"  Files skipped:       {skipped}");
Console.WriteLine($"  Types left in place: {typesSkipped} (contain #region / #if directives)");
Console.WriteLine($"  Errors:              {errors}");
ConsoleOutput.PrintDivider();

if (report)
{
    string resolvedReportPath = reportPath is not null
        ? Path.GetFullPath(reportPath)
        : Path.Combine(Path.GetFullPath(target), "freecodemaid-report.md");
    int typesReorderedTotal = reportEntries.Sum(e => e.Result.TypesReordered);
    string markdown = ReportWriter.Build(
        Path.GetFullPath(target), apply, changed, typesReorderedTotal, bracesTotal, reportEntries);
    try
    {
        File.WriteAllText(resolvedReportPath, markdown, new UTF8Encoding(false));
        Console.WriteLine($"Report written to: {resolvedReportPath}");
    }
    catch (Exception ex)
    {
        ConsoleOutput.WriteLine($"  ERROR writing report to {resolvedReportPath}: {ex.Message}", isError: true);
    }
}

if (errors > 0)
{
    return 2;
}
if (!apply && changed > 0)
{
    return 1; // CI gate: reorganization is needed
}
return 0;

static bool RunDotnetFormat(string root)
{
    try
    {
        var psi = new System.Diagnostics.ProcessStartInfo
        {
            FileName = "dotnet",
            UseShellExecute = false,
            WorkingDirectory = root
        };
        psi.ArgumentList.Add("format");
        psi.ArgumentList.Add("whitespace");
        psi.ArgumentList.Add(root);
        psi.ArgumentList.Add("--folder");
        psi.ArgumentList.Add("--exclude");
        psi.ArgumentList.Add("**/obj/**");
        psi.ArgumentList.Add("**/bin/**");

        using var proc = System.Diagnostics.Process.Start(psi);
        if (proc is null)
        {
            return false;
        }
        proc.WaitForExit();
        return proc.ExitCode == 0;
    }
    catch (Exception ex)
    {
        ConsoleOutput.WriteLine("  WARNING: could not run 'dotnet format': " + ex.Message, isError: true);
        return false;
    }
}

static string DescribeChange(ReorgResult r)
{
    var parts = new List<string>();
    if (r.TypesReordered > 0)
    {
        parts.Add(r.TypesReordered + " type(s) reordered");
    }
    if (r.BracesCollapsed > 0)
    {
        parts.Add(r.BracesCollapsed + " brace(s) collapsed");
    }
    return parts.Count > 0 ? string.Join(", ", parts) : "formatting";
}

static bool LooksAutoGenerated(string text)
{
    var head = text.Length > 400 ? text[..400] : text;
    return head.Contains("<auto-generated", StringComparison.OrdinalIgnoreCase)
        || head.Contains("This file was generated", StringComparison.OrdinalIgnoreCase)
        || head.Contains("This code was generated", StringComparison.OrdinalIgnoreCase);
}

static void WritePreservingBom(string path, string text)
{
    var bytes = File.ReadAllBytes(path);
    bool hasBom = bytes.Length >= 3 && bytes[0] == 0xEF && bytes[1] == 0xBB && bytes[2] == 0xBF;
    File.WriteAllText(path, text, new UTF8Encoding(hasBom));
}

static void PrintHelp()
{
    ConsoleOutput.PrintBanner("FreeCodeMaid", "1.0");
    Console.WriteLine("A Roslyn-based C# standardizer — the gaps that .editorconfig and 'dotnet format'");
    Console.WriteLine("cannot fill. In one command (with --apply) it runs 'dotnet format whitespace' for");
    Console.WriteLine("cleanup, then reorganizes class members (preserving comment/doc blocks) and");
    Console.WriteLine("restores the author's wrapped-parameter \"){\" brace style.");
    Console.WriteLine();
    Console.WriteLine("USAGE:");
    Console.WriteLine("  FreeCodeMaid <path> [options]");
    Console.WriteLine();
    Console.WriteLine("  <path>   A .cs file, a directory, or a .sln/.slnx/.csproj. Defaults to the");
    Console.WriteLine("           current directory.");
    Console.WriteLine();
    Console.WriteLine("OPTIONS:");
    Console.WriteLine("  --apply, --write   Rewrite files in place. Without this, runs as a dry run.");
    Console.WriteLine("  --check            Dry run (default): report what would change; exit 1 if any.");
    Console.WriteLine("  --config=<file>    Use a specific freecodemaid.json. Otherwise the tool searches");
    Console.WriteLine("                     upward from <path> for one, then falls back to built-in defaults.");
    Console.WriteLine("  --no-format        Skip the 'dotnet format whitespace' cleanup step (reorganize only).");
    Console.WriteLine("  --report[=<file>]  Write a Markdown report of exactly what changed. With no path,");
    Console.WriteLine("                     writes <path>\\freecodemaid-report.md. Off by default.");
    Console.WriteLine("  --verbose, -v      List skipped/unchanged files too.");
    Console.WriteLine("  --help, -h         Show this help.");
    Console.WriteLine();
    Console.WriteLine("EXIT CODES:  0 = clean/applied   1 = changes needed (check mode)   2 = error");
}
